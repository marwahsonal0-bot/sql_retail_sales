--Q.1 Write a SQL query to find the total revenue generated by the business

SELECT SUM(total_sale) AS total_revenue
FROM retail_sales;

--Q.2 Write a SQL query to find which product categories generate the highest revenue

SELECT category,
       SUM(total_sale) AS total_revenue
FROM retail_sales
GROUP BY category
ORDER BY total_revenue DESC
LIMIT 1;

